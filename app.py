"""
app.py
------
Streamlit web application for the "Student Performance Prediction"
project. Provides an interactive form where a user enters a student's
details and instantly receives a Pass / Average / Fail prediction,
along with the model's confidence and overall test-set accuracy.

Run with:
    streamlit run app.py
"""

import os
import sys
import pandas as pd
import streamlit as st

sys.path.append(os.path.join(os.path.dirname(__file__), "src"))
from predict import predict_student

BASE = os.path.dirname(os.path.abspath(__file__))
REPORTS_DIR = os.path.join(BASE, "outputs", "reports")

st.set_page_config(
    page_title="Student Performance Prediction",
    page_icon="🎓",
    layout="wide",
)

# ---------------------- Sidebar ----------------------
with st.sidebar:
    st.title("🎓 Student Performance Predictor")
    st.markdown(
        """
        This app predicts whether a student will **Pass**, achieve an
        **Average** result, or **Fail**, based on academic and
        lifestyle factors, using a Machine Learning model trained on
        synthetic student data.
        """
    )
    st.markdown("---")
    st.subheader("📊 Model Info")

    metrics_path = os.path.join(REPORTS_DIR, "best_model_metrics.csv")
    if os.path.exists(metrics_path):
        metrics_df = pd.read_csv(metrics_path)
        model_name = metrics_df.loc[0, "Model"]
        accuracy = metrics_df.loc[0, "Accuracy"]
        st.metric("Best Model", model_name)
        st.metric("Test Accuracy", f"{accuracy * 100:.2f}%")
    else:
        st.info("Run `python src/run_pipeline.py` first to train the model.")

    st.markdown("---")
    st.caption("Built with Python, Scikit-learn & Streamlit")

# ---------------------- Main Title ----------------------
st.title("🎓 Student Performance Prediction Using Machine Learning")
st.markdown(
    "Enter a student's academic and lifestyle details below to predict "
    "their likely final result."
)

st.markdown("---")

# ---------------------- Input Form ----------------------
with st.form("prediction_form"):
    col1, col2, col3 = st.columns(3)

    with col1:
        st.subheader("👤 Personal Info")
        gender = st.selectbox("Gender", ["Male", "Female"])
        age = st.slider("Age", 16, 19, 18)
        family_support = st.selectbox("Family Support", ["Low", "Medium", "High"], index=1)

    with col2:
        st.subheader("📚 Academic Info")
        attendance = st.slider("Attendance (%)", 0.0, 100.0, 75.0)
        study_hours = st.slider("Study Hours Per Day", 0.0, 12.0, 3.0, step=0.5)
        previous_marks = st.slider("Previous Marks (%)", 0.0, 100.0, 60.0)
        assignment_score = st.slider("Assignment Score (%)", 0.0, 100.0, 65.0)
        internal_marks = st.slider("Internal Marks (out of 40)", 0.0, 40.0, 25.0)

    with col3:
        st.subheader("🌙 Lifestyle Info")
        sleep_hours = st.slider("Sleep Hours", 3.0, 10.0, 6.5, step=0.5)
        internet_usage = st.slider("Internet Usage (non-study, hrs/day)", 0.0, 10.0, 3.0, step=0.5)
        participation = st.selectbox("Class Participation", ["Low", "Medium", "High"], index=1)

    submitted = st.form_submit_button("🔮 Predict Result", use_container_width=True)

# ---------------------- Prediction Output ----------------------
if submitted:
    record = {
        "Gender": gender,
        "Age": age,
        "Attendance": attendance,
        "Study_Hours_Per_Day": study_hours,
        "Previous_Marks": previous_marks,
        "Assignment_Score": assignment_score,
        "Internal_Marks": internal_marks,
        "Sleep_Hours": sleep_hours,
        "Internet_Usage": internet_usage,
        "Participation": participation,
        "Family_Support": family_support,
    }

    try:
        label, proba = predict_student(record)

        st.markdown("---")
        st.subheader("📋 Prediction Result")

        color_map = {"Pass": "green", "Average": "orange", "Fail": "red"}
        st.markdown(
            f"<h2 style='color:{color_map.get(label, 'black')};'>"
            f"Predicted Result: {label}</h2>",
            unsafe_allow_html=True,
        )

        if proba:
            st.write("**Prediction Confidence:**")
            proba_df = pd.DataFrame(list(proba.items()), columns=["Result", "Probability"])
            proba_df["Probability"] = proba_df["Probability"] * 100
            st.bar_chart(proba_df.set_index("Result"))

        st.info(
            "Note: This prediction is generated by a machine learning model "
            "trained on synthetic academic data for educational purposes, "
            "and should not be treated as a guaranteed outcome."
        )

    except FileNotFoundError:
        st.error(
            "Model artifacts not found. Please run "
            "`python src/run_pipeline.py` first to train and save the model."
        )
    except Exception as e:
        st.error(f"Something went wrong while predicting: {e}")

st.markdown("---")
st.caption("Student Performance Prediction | AI & ML Internship Project")
